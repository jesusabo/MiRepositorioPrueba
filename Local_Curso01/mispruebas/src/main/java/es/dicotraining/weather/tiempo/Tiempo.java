package es.dicotraining.weather.tiempo;

public class Tiempo {
	
	 private int code;
	 private int date;
	 private int day;
	 private int high;
	 private int low;
	 private int text;
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public int getDate() {
		return date;
	}
	public void setDate(int date) {
		this.date = date;
	}
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	public int getHigh() {
		return high;
	}
	public void setHigh(int high) {
		this.high = high;
	}
	public int getLow() {
		return low;
	}
	public void setLow(int low) {
		this.low = low;
	}
	public int getText() {
		return text;
	}
	public void setText(int text) {
		this.text = text;
	}
	 
	 

}
